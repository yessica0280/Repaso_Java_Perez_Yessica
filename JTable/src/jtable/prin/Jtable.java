    void consultar(){
        String sql = "select * from Usuarios";
        try {
            conet = con1.getConnection();
            st = conet.createStatement();
            rs = st.executeQuery(sql);
            Object[] usuarios = new  Object[3];
            modelo = (DefaultTableModel) Tabla.getModel();
            while (rs.next()) {
                usuarios [0] = rs.getInt("Id: ");
                usuarios [1] = rs.getString("Nombre: ");
                usuarios [2] = rs.getString("Email: ");
                modelo.addRow(usuarios);
            }
            Tabla.setModel(modelo);
        }
        catch (Exception e) {
            
        }
    }
    
    void Agregar() {
        String nom = txtNombre.getText();
        String email = txtEmail.getText();
        
        try {
            if (nom.equals("") || email.equals("")) {
                JOptionPane.showMessageDialog(null, "Faltan ingresar algunos datos.");
                limpiarTabla();
            }
            else {
                String sql = "inset into usuarios(nombre, email) values ('"+nom+"', '"+email+"')";
            conet = con1.getConnection();
            st = conet.createStatement();
            st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Nuevo usuario agregado con exito.");
                limpiarTabla();
            }
        }
        catch (Exception e) {
        
        }
    }
    
    void limpiarTabla() {
        for (int i = 0; i <= Tabla.getRowCount(); i ++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

    void Eliminar() {
        int fila = Tabla.getSelectedRow();
        try {
            if (fila < 0) {
                JOptionPane.showMessageDialog(null, "Usuario no seleccionado.");
                limpiarTabla();
            }
            else {
                String sql = "delete from usuarios where id= " + idc;
            conet = con1.getConnection();
            st = conet.createStatement();
            st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Usuario eliminado con exito.");
                limpiarTabla();
            }
        }
        catch (Exception e) {
        
        }
    }
    
